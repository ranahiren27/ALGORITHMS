
def mergeSort(alist):
    print("Splitting ",alist)
    if len(alist)>1:
        mid = len(alist)//2
        lefthalf = alist[:mid]
        righthalf = alist[mid:]

        mergeSort(lefthalf)
        mergeSort(righthalf)

        i=0
        j=0
        k=0
        while i < len(lefthalf) and j < len(righthalf):
            if lefthalf[i] < righthalf[j]:
                alist[k]=lefthalf[i]
                i=i+1
            else:
                alist[k]=righthalf[j]
                j=j+1
            k=k+1

        while i < len(lefthalf):
            alist[k]=lefthalf[i]
            i=i+1
            k=k+1

        while j < len(righthalf):
            alist[k]=righthalf[j]
            j=j+1
            k=k+1
        print("Merging",alist)
        
    

LIST = []
n = int(input("Enter the number of elements to be sorted:"))
print("Enter %s numbers in randomly sorted order:" % str(n))

for i in range(n):
    LIST.append(int(input()))

mergeSort(LIST)

print("The sorted list is as follows:-")

for i in range(n):
    print(LIST[i])



    
